package pe.upc.repetitivas;

public class App5 {
    public static void main(String[] args) {
        int i=1;
        do{
            System.out.println(i);//1,2,3,...9, 10
            i++;//2,3,4,..9, 10,11
        }while(i<=10);//mientras es verdad repite
    }
    //cuando una estructura repetitiva esta dentro de otra
    //a eso se le llama sestructura "anidada"
}
