package pe.upc.repetitivas;

public class App1 {
    public static void main(String[] args) {
        for(int i=0; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("Continua normal.....");
        int i=0;
        while(i<=10){ //mientras condicion es verdadera se repite
            System.out.println(i);
            i++;
        }
    }
}
